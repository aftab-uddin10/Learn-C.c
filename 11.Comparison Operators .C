/*  Operator	  Name     	      Example	
     ==	      Equal to	        x == y	
     !=	      Not equal  	       x != y	
     >	    Greater than	        x > y	
     <	    Less than              x < y		
   >=	  Greater than or equal to    	x >= y	
   <=	   Less than or equal to	      x <= y	
*/


//Example
int x = 5;
int y = 3;
